package mai.linh.junit;

/**
 * Calculator
 */
public class Calculator {

	public int plus(int a, int b) {
		return a + b;
	}

	public int minus(int a, int b) {
		return a - b;
	}

	public int multiply(int a, int b) {
		return a * b;
	}

	public int devide(int a, int b) {
        return a / b;
	}
    
}